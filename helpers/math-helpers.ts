export function addTwoNumbers(a: number, b: number): number {
  return  (a + b);
}

export function subtractTwoNumbers(a: number, b: number): number {
  return  (a - b);
}

export function timesNumbers(a: number, b: number): number {
  return  (a * b);
}

export function divideNumbers(a: number, b: number): number {
  return  (a / b);
}